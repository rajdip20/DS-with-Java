public class ArithmeticOperators {
    public static void main(String[] args) {
        int p = 20, q = 10;

        System.out.println(p + q);  // 30
        System.out.println(p - q);  // 10
        System.out.println(p * q);  // 200
        System.out.println(p / q);  // 2
        System.out.println(p % q);  // 0

    }
}
