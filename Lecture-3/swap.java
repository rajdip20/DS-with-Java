public class swap {
    public static void main(String[] args) {
        int num1 = 2, num2 = 3, num3;
        System.out.println("Before swaping, num1 = " + num1 + " and num2 = " + num2);
        num3 = num1;
        num1 = num2;
        num2 = num3;
        System.out.println("After swaping, num1 = " + num1 + " and num2 = " + num2);

    }
}
